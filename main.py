def menu():
    clear()
    print_ascii_art()
    print("================ Menu principal ===============")
    print("1. Scanner de Rede (Ping Sweep)")
    print("2. Verificar DNS")
    print("3. Scan de Portas")
    print("4. ARP Scan")
    print("5. Traceroute")
    print("6. Verificar portas comuns em todos os hosts")
    print("7. Sniffer de tráfego de rede")
    print("8. Detectar ARP Spoofing")
    print("9. Testar conexão com serviços externos")
