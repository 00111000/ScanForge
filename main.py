def main():
    while True:
        clear()
        menu()
        opcao = input("Escolha uma opção: ")

        if opcao == '1':
            ping_sweep()
        elif opcao == '2':
            dns_check()
        elif opcao == '3':
            port_scan()
        elif opcao == '4':
            arp_scan()
        elif opcao == '5':
            traceroute()
        elif opcao == '6':
            portas_comuns()
        elif opcao == '7':
            sniffer_start()
        elif opcao == '8':
            detectar_arp_spoofing()
        elif opcao == '9':
            testar_conexao_externa()
        elif opcao == '10':
            buscar_compartilhamentos()
        elif opcao == '11':
            teste_latencia()
        elif opcao == '12':
            print("Encerrando...")
            break
        else:
            print("Opção inválida.")

        input("\nPressione ENTER para continuar...")

if __name__ == "__main__":
    main()
